                                        <h3> Biological </h3>
                                        <p>
                                            Biological differences in genetic makeup can make one person more susceptible to addiction than another. Contrary to popular belief, there is not one single “addiction gene”. In fact, many genes influence addiction. Different levels of dopamine receptors in people also can influence susceptibility to addiction. If a person has a quite high number or receptors, then the drug will feel much better to them than another person with perhaps a lower number of dopamine receptors, and so the first person is more susceptible to addiction.

                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="black-bg col-sm-12 col-md-12 col-lg-12">
                                        <h3> Psychological </h3>
                                        <p>
                                           The cause of addiction can also be psychologically based. Some people see doing a drug as a way to cope with uncomfortable feelings or stress. Substance abuse can also begin by trying to alleviate psychological issues as a form of self-medication. These psychological issues include but are not limited to schizophrenia, depression, anxiety, bipolar, or personality disorders such as antisocial personality disorder.
                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="black-bg col-sm-12 col-md-12 col-lg-12">
                                        <h3> Environmental / Social </h3>
                                        <p>
                                            The environment one is in can affect one’s susceptibility to drug addiction. Some of these social factors include stress, early physical or sexual abuse, witnessing violence, peers who use drugs, and drug availability.
                                        </p>
