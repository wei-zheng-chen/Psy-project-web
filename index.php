                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Alcohol Rehab Strategies/Methods</h3>
                                        <p>
                                            For any addictive behavior, the most effective means of treatment include a combination of both pharmacological and behavioral therapy. Medications can be prescribed such as Acamprosate, Natrexone, Disulfiram, and Baclofen. These drugs are prescribed to either ease alcohol cravings, cause unpleasant symptoms if alcohol is drunk, or to reduce alcohol-related anxiety. Behavioral therapy is also recommended. Such therapies can include group therapy like Alcoholics Anonymous and other 12-step programs, which have proven quite successful. In choosing a rehabilitation plan, it is also important to decide if either a long term or short term rehabilitation center is right for the addict. 
                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Heroin Rehab Strategies/Methods</h3>
                                        <p>
                                            Medication (pharmacological) and behavioral treatments have both been proven effective, yet in many cases a combination of the two can lead to the most success. There are three options for types of medications to give patients for pharmacological treatments: agonists, partial agonists and antagonists. Agonists (for example Methadone) activate the same opioid receptors in the brain that heroin does. Partial agonists (for example Buprenorphine) activate the opioid receptors, but with a smaller response. Antagonists (Naltrexone) blocks receptor, interfering with the rewarding effects of activating opioid receptors. In terms of behavioral therapy, Cognitive-Behavioral Therapy (CBT) has proven very useful in treatment programs, altering the addicts thinking about their addictive behavior and addressing stressors in life that can lead to drug abuse. Another method of behavioral therapy that has been proven quite successful is called Contingency Management, in which patients receive rewards for good behaviors. These rewards encourage healthy living.
                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Cocaine Rehab Strategies/Methods</h3>
                                        <p>
                                            Like with other addictive substances, a combination of pharmacological and behavioral rehabilitation strategies has been proven most successful in helping addicts overcome their addictions. Unfortunately for cocaine addicts, no FDA approved medications are currently on the market to treat cocaine addiction. Much research is currently being done to find a drug to assist cocaine addiction, but no solutions have been found yet although some medicines used for other diseases such as alcoholism have showed some promise in clinical trials. Behavioral therapy is currently the only form of therapy for stimulant addictions. Community based rehabilitation options have been proven quite effective, for example Cocaine/Narcotics Anonymous or community residential programs. Cognitive behavioral therapy is also quite effective. In CBT, therapists teach addicts to recognize feelings/situations in which they might relapse, avoid these situations when they can, and cope better with the problems associated with drug abuse. The final behavioral therapy which has been proven effective is contingency management, which is a reward system for patients who do not relapse. 
                                        </p>
