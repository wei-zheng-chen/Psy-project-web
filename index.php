                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Results of Alcohol Addiction </h3>
                                        <p>
                                           Alcohol addiction causes permanent changes to the brain. It does so by altering your brain chemistry. Long-term chemical changes in the brain are thought to be responsible for alcohol dependence. The method in which alcohol addiction alters your brain function is through neurotransmitters. A neurotransmitter is a chemical in the brain that serves as a messenger of signals between neurons and other cells in the body. They normally help regulate body’s function and behavior. While a person is intoxicated, it alters the balance of neurotransmitter chemicals. With long-term intoxication, the brain tries to alter its chemistry so that the brain will function closer to normal while intoxicated. The physical long term effects of alcohol abuse can include anemia (loss of oxygen-carrying red blood cells), cirrhosis (liver becomes unable to function), dementia, depression, seizures, gout, and pancreatitis. 
                                        </p>
                                    </div>
                                </div>

                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3 >Results of Heroin Addiction</h3>
                                        <p>
                                            Like alcohol addiction, heroin addiction permanently changes the physiology of the brain, and these changes are not easily reversed. Heroin addictions creates tolerance, meaning you need more and more of the drug in order to get to the same level of high. Heroin also causes a strong physical dependence. Having a physical dependence on a drug causes withdrawal symptoms to begin after abrupt discontinuation or reduction of dosage. Specifically, heroin binds to the receptors in the brain called mu-opiod receptors (MORs). Normally our bodies natural neurotransmitters bind to these MORs, but when heroin is taken, this external drug binds to the MORs releasing dopamine in the brain. A release of dopamine in the brain increases feelings of pleasure, and blocks feelings of pain, among other things. Heroin can have many detrimental effects on the body as well. Long term use can lead to reduced sexual capacity, depression, loss of appetite, weakening of the immune system, bad teeth, insomnia, loss of memory, arthritis, and respiratory illnesses. By injecting heroin, a whole new slew of results can occur, such as collapsed veins leading to infections of the blood vessels and also sharing needles can lead to AIDS and other contagious infections such as Hepatitis. 
                                        </p>
                                    </div>
                                </div>

                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3 >Results of Cocaine Addiction</h3>
                                        <p>
                                            As a result of Cocaine abuse, addicts experience both neurological and physical negative effects. The addict’s brain adapts to the repeated use of the drug and reward pathway becomes less sensitive to the drug. This phenomena is known as tolerance. It also causes the addicts brain to become less sensitive to the natural reinforcers of the reward pathway themselves. With cocaine addiction, the most affected part of the brain is a region of midbrain called the ventral tegmental area (VTA). Cocaine blocks removal of dopamine from synapses in the brain. This results in an accumulation of dopamine in the brain and amplified signal to receiving neurons known as dopamine receptors. Another phenomena called sensitization can also occur. This means that the addict becomes more sensitive to cocaine’s effect as time increases. Long term use can lead to irritability, paranoia, panic attacks, restlessness, and psychosis. Also, different methods of ingesting the drug can cause their own results. For example, snorting the drug can lead to loss of smell, hoarseness, nosebleeds, problems swallowing, and chronically inflamed and runny nose. There are different specific effects for ingested cocaine, and injected cocaine. Although no matter which manner you ingest the drug, loss of appetite, significant weight loss and malnourishment occur in many chronic users.
                                        </p>
