                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Alcohol Withdrawal</h3>
                                        <p>
                                            The decision to get sober can be made much more difficult for any addict because of the inevitable withdrawal symptoms that come along with stopping use of a drug. Alcohol withdrawal in particular causes symptoms like nausea, trembling, sweating, extreme alcohol cravings, and increased heart rate. These withdrawal symptoms can last around five to seven days after the addict’s last drink. The exact withdrawal symptoms a person will experience can be unique to the individual. Some have very mild symptoms, and others can have more serious symptoms such as delirium and marked tremors.
                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Heroin Withdrawal</h3>
                                        <p>
                                            Withdrawal symptoms for heroin addicts have a much faster onset than most other drugs. Heroin withdrawal symptoms can start as early as a few hours after the last dose of the drug is ingested. Symptoms are the worst between 24-48 hours after the last dose. Symptoms can last up to a week. The specific symptoms addicts experience can include restlessness, muscle and bone pain, insomnia, diarrhea, vomiting, cold flashes, and leg movements.
                                        </p>
                                    </div>
                                </div>
                                <div class="col-sm-12 col-md-12 col-lg-12 margin-bottom-20">
                                    <div class="col-sm-12 col-md-12 col-lg-12 black-bg">
                                        <h3>Cocaine Withdrawal</h3>
                                        <p>
                                            Once an addict stops taking cocaine, withdrawal symptoms begin. These symptoms include strong cravings, fatigue, anxiety, irritability, exhaustion, inability to feel pleasure from a normal source, depression, paranoia, chills, body aches, and tremors. The beginning of withdrawl is marked by a crash, which is very similar to the exact opposite feeling the user feels when they ingest the drug. Withdrawal symptoms can last 1-2 weeks after the last dose of the drug is taken. 
                                        </p>
